// 1. create 2 variables that have values of user input
const numOne = prompt("Gimme a number...");
const numTwo = prompt("Gimme another number...");

console.log(typeof numOne, typeof numTwo);

// Data Types
// 1. Convert the number variables to numbers
const convertedNumOne = Number(numOne);
const convertedNumTwo = Number(numTwo);

// 2. Do a check to ensure the input are actual numbers
console.log(
  typeof convertedNumOne === "number",
  typeof convertedNumTwo === "number"
);

// Operations of Data and Strings
// 1. console log the value of the numbers added together
console.log(convertedNumOne + convertedNumTwo);
// 2. make a string using the variables and console log the value
console.log(`The value is: ${convertedNumOne * convertedNumTwo}`);

